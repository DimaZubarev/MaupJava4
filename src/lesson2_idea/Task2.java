package lesson2_idea;

public class Task2 {
    public static void main(String[] args) {
        double d = 2.6;
        int i = (int)(d + 0.5);
        short f = 500;
        short g = (short)(f + 300);

        System.out.println("i = " + i);
        System.out.println("(int)2.6 = " + (int)2.6);
        System.out.println("(double)3 = " + (double)3);
        System.out.println("(int)'A' = " + (int)'A');
        System.out.println("short g = " + g);
    }
}
