package lesson2_idea;

public class TestMath {
    public static void main(String[] args) {
        int i = 3;
        double d = 3.3;
        System.out.println(Math.abs(i));
        System.out.println(Math.round(i));
        System.out.println(Math.ceil(d));
        System.out.println(Math.cos(d));
        System.out.println(Math.sin(d));
        System.out.println(Math.atan(d));
        System.out.println(Math.toDegrees(d));
        System.out.println(Math.toRadians(d));
        System.out.println(Math.sqrt(i));
        System.out.println(Math.pow(i, 25));
    }
}
